CREATE FUNCTION PF.VerificarNúmero(@Número int) RETURNS INT
AS
	BEGIN
		DECLARE @count	INT
		SELECT @count=COUNT(1) FROM PF.ENCOMENDA WHERE Número=@Número
		RETURN @COUNT
	END
GO